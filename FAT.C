void PrepareBPB()
{
  // Initialize pointers to values that need to be redefined
  unsigned int* pBytesPerSector = (unsigned int*)(&(pDMABuffer)[0x0b]);
  unsigned char* pSectorsPerCluster = (unsigned char*)(&(pDMABuffer)[0x0d]);  
  unsigned int* pReservedSectors = (unsigned int*)(&(pDMABuffer)[0x0e]);
  unsigned int* pRootDirEntries = (unsigned int*)(&(pDMABuffer)[0x11]);
  unsigned int* pTotalSectors = (unsigned int*)(&(pDMABuffer)[0x13]);
  unsigned char* pMediaDescriptor = (unsigned char*)(&(pDMABuffer)[0x15]);
  unsigned int* pSectorsPerFAT = (unsigned int*)(&(pDMABuffer)[0x16]);
  unsigned int* pSectorsPerTrack = (unsigned int*)(&(pDMABuffer)[0x18]);
  unsigned int* pNumberOfHeads = (unsigned int*)(&(pDMABuffer)[0x1a]);
  
  // Initialize with default values (5,25" 360K) and start defining the 8" geometry
  memcpy(pDMABuffer, sBIOSParameterBlock, sizeof(sBIOSParameterBlock));
  
  // Common values for 77-track 8"s
  *pNumberOfHeads = (unsigned int)nHeads; 
  *pSectorsPerTrack = (unsigned int)nSectorsPerTrack;
  *pBytesPerSector = nSectorSize;
  *pTotalSectors = (unsigned int)nSectorsPerTrack * (unsigned int)nHeads * (unsigned int)nTracks;
  
  // Single sided, single density
  if ((nHeads == 1) && (nDoubleDensity == 0))
  {
    *pSectorsPerCluster = 4;
    *pReservedSectors = 1;
    *pRootDirEntries = 68;
    *pMediaDescriptor = 0xfe;
    *pSectorsPerFAT = 6;
  }
  
  // Double sided, single density
  else if ((nHeads == 2) && (nDoubleDensity == 0))
  {
    *pSectorsPerCluster = 4;
    *pReservedSectors = 4;
    *pRootDirEntries = 68;
    *pMediaDescriptor = 0xfd;
    *pSectorsPerFAT = 6;
  }
  
  // Double sided, double density
  else
  {
    *pSectorsPerCluster = 1;
    *pReservedSectors = 1;
    *pRootDirEntries = 192;
    *pMediaDescriptor = 0xfe;
    *pSectorsPerFAT = 2;
  }
}

void WriteBootCode()
{
  // Write a single 1024-byte bootsector, or four 128-byte sectors.
  unsigned char* pAfterBPB = &(pDMABuffer)[0x36];
  
  // Prepare the BIOS parameter block (drive geometry)
  PrepareBPB();
  
  // 128-byte-per-sector floppy requires a special "bootstrapper"
  if (nSectorSize == 128)
  {
    unsigned char nSectorIdx = 1;
    unsigned int nBytesWritten = 0;
    
    // The BIOS only loads one sector CHS 0/0/1. That's 128 bytes in code: too small to load anything
    // So the 128byte bootstrap loads 3 more sectors afterwards, to get a complete 512B boot routine.
    memcpy(pAfterBPB, s128ByteBootstrapper, nSectorSize-sizeof(sBIOSParameterBlock));
    
    // On track 0, head 0
    FDDSeek(0, 0);
    
    for(;;)
    {
      // Complete boot routine
      unsigned int nRemainingBytes = sizeof(s128ByteBootstrapper) - nBytesWritten;
      
      // Write one 128B sector
      FDDWrite(nSectorIdx++);
      
      // The first part is 74byte long + 54 bytes BPB (128byte in sum) 
      if (nBytesWritten == 0)
      {
        nBytesWritten += 74;
      }
      else
      {
        nBytesWritten += nSectorSize; //128
      }
      
      if (nRemainingBytes == 0)
      {
        // Done
        break;
      }
      
      // Next 128byte part      
      memcpy(pDMABuffer,
             &(s128ByteBootstrapper)[nBytesWritten],
             (nRemainingBytes > nSectorSize) ? nSectorSize : nRemainingBytes); 
    }
  }
  
  // Classic bootsector code (fits a >=512-byte sector)
  else if (nSectorSize >= 512)
  {
    // Needs to be signed manually at the end of the sector
    unsigned int* pSignatureBPB = (unsigned int*)(&(pDMABuffer)[nSectorSize-2]);
    
    // Copy classic bootsector
    memcpy(pAfterBPB, sFAT12BootCode, sizeof(sFAT12BootCode));
    
    // Bootable sector signature for BIOS
    *pSignatureBPB = 0xaa55;
    
    // Write one 1024B sector on track 0, head 0, sector no: 1
    FDDSeek(0, 0);
    FDDWrite(1);
  }  
}

void WriteRootDir()
{
  // Write an empty root directory (6KB) with signature
  // Starting at sector 2 (1024B size) or 5 (128B size)  
  unsigned char nSectorIdx = (nSectorSize == 1024) ? 2 : 5;
  unsigned int nBytesWritten = 0;
  
  while(nBytesWritten != 6*1024)
  {
    static unsigned char nHead = 0;
    static unsigned char nTrack = 0;
    
    memset(pDMABuffer, 0, nSectorSize);
    
    // Add signature
    if ((nBytesWritten == 0) || (nBytesWritten == 1024))
    {
      memcpy(pDMABuffer, sRootDir, sizeof(sRootDir));
    }
    
    FDDWrite(nSectorIdx++);    
    
    // Need to advance to the next head (or track) ?
    if (nSectorIdx > nSectorsPerTrack)
    {
      nSectorIdx = 1;
      nHead++;
      
      if (nHead > nHeads-1)
      {
        nHead = 0;
        FDDSeek(++nTrack, nHead);     
      }
      else
      {
        FDDSeek(nTrack, nHead);
      }
    }
    
    nBytesWritten += nSectorSize;
  }
}

void WriteFAT12()
{     
  // Clear the 1K DMA buffer
  memset(pDMABuffer, 0, 1024);
  
  // Write FAT12 bootsector 
  WriteBootCode();

  // Write an empty root directory
  WriteRootDir();
}